public class Reassigning {
    
    public static void main( String []args){
        double x= 3.21;
        double answer = 3*Math.pow( x, 2) -8*x+4;

        double x2= 460;
        double answer2 = 3*Math.pow( x2, 2) -8*x2+4;

        double x3= 0;
        double answer3 = 3*Math.pow( x3, 2) -8*x3+4;

        System.out.println("At x = " + x);
        System.out.println("The Value is " + answer);

        System.out.println("             ");

        System.out.println("At x2 = " + x2);
        System.out.println("The Value is " + answer2);

        System.out.println("              ");
        
        System.out.println("At x3 = " + x3);
        System.out.println("The Value is " + answer3);


       
    }




}